export type SeoTranslationsObject = (typeof seoTranslations)['en'] &
  typeof enTranslations
export type SeoTranslationsKeys = NestedKeysStripped<SeoTranslationsObject>

/* Field-config helper. Returns a Payload label/description function that
   resolves a `pluginSeo:` key with the request's `t` (typed to our key union).
   Node-safe: this module imports only data + types, no `@payloadcms/ui`. */
export const seoT =
  (key: SeoTranslationsKeys) =>
  ({ t }: { t: TFunction }): string =>
    (t as TFunction<SeoTranslationsKeys>)(key)

/* Static (locale-keyed object) form of a `pluginSeo:` string, for the few slots
   that must be plain-serializable rather than a label/description FUNCTION —
   notably a GLOBAL's entity-level `admin.description`/`label`. Payload's global
   client-config builder copies those RAW across the RSC→client boundary (it
   resolves function descriptions for collections and fields, but NOT for
   globals), so a function there throws "Functions cannot be passed to Client
   Components". A locale-keyed object is serializable and is resolved to the
   active admin language by Payload's `getTranslation` / our i18n-aware
   extraction, exactly like any other locale-keyed label. */
export const seoStatic = (key: SeoTranslationsKeys): Record<string, string> => {
  const sub = key.slice('pluginSeo:'.length)
  const pick = (lang: keyof typeof seoTranslations): string =>
    (seoTranslations[lang].pluginSeo as Record<string, string>)[sub] ?? sub
  return { en: pick('en'), fr: pick('fr'), de: pick('de'), es: pick('es') }
}
